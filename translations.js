// Language detection and switching functionality
class I18n {
    constructor() {
        this.currentLang = this.detectLanguage();
        this.version = 'v1.0.0'; // Default version
        this.init();
    }

    detectLanguage() {
        // Check localStorage first
        const savedLang = localStorage.getItem('vexar-lang');
        if (savedLang && translations[savedLang]) {
            return savedLang;
        }

        // Detect browser language
        const browserLang = navigator.language || navigator.userLanguage;
        const langCode = browserLang.split('-')[0].toLowerCase();

        // Return Turkish if detected, otherwise English
        return translations[langCode] ? langCode : 'en';
    }

    init() {
        this.applyTranslations();
        this.updateLangSwitcher();
        this.setupLangSwitcher();
    }

    applyTranslations() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            let translation = this.getTranslation(key);
            if (translation) {
                // Handle placeholders like {version}
                if (translation.includes('{version}')) {
                    translation = translation.replace('{version}', this.version);
                }

                // Check if it contains HTML (like <br>)
                if (translation.includes('<')) {
                    el.innerHTML = translation;
                } else {
                    el.textContent = translation;
                }
            }
        });

        // Update document title
        document.title = this.getTranslation('meta.title') || document.title;

        // Update HTML lang attribute
        document.documentElement.lang = this.currentLang;
    }

    getTranslation(key) {
        return translations[this.currentLang]?.[key] || translations['en']?.[key] || key;
    }

    updateLangSwitcher() {
        const langText = document.getElementById('currentLang');
        if (langText) {
            langText.textContent = this.currentLang.toUpperCase();
        }
    }

    setupLangSwitcher() {
        const switcher = document.getElementById('langSwitcher');
        if (switcher) {
            switcher.addEventListener('click', () => {
                this.toggleLanguage();
            });
        }
    }

    toggleLanguage() {
        this.currentLang = this.currentLang === 'en' ? 'tr' : 'en';
        localStorage.setItem('vexar-lang', this.currentLang);
        this.applyTranslations();
        this.updateLangSwitcher();

        // Add a subtle animation to indicate change
        document.body.style.opacity = '0.9';
        setTimeout(() => {
            document.body.style.opacity = '1';
        }, 150);
    }

    setLanguage(lang) {
        if (translations[lang]) {
            this.currentLang = lang;
            localStorage.setItem('vexar-lang', lang);
            this.applyTranslations();
            this.updateLangSwitcher();
        }
    }

    setVersion(version) {
        this.version = version;
        this.applyTranslations();
    }
}

// Export for use in main script
window.I18n = I18n;
window.translations = translations;
